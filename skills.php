    <h3>CSS</h3>
    <img src = "css3.png" alt = "CSS" style="float:left;width:150px;height:150px;">
    <p style="text-align:left;"> CSS ("Cascading Style Sheets") is the language we use to style a Web page.
It describes how HTML elements are to be displayed on screen, paper, or in other media. 
It saves a lot of work in external stylesheets that can control the layout of multiple web pages all at once.
 </p>
    <h3 style="text-align:right;">PHP</h3>
    <img src = "php.jpg" alt = "PHP" style="float:right;width:250px;height:100px;">
    <p> PHP ("PHP: Hypertext Preprocessor") is a widely-used, open source server-side scripting language that executes scripts on the server.
        It is free to download and is used for a variety of purposes, including generating dynamic page content
processing and modifying files on the server, collecting form data,
sending and receiving cookies, and analyzing and encrypting data in the database.
 </p>
